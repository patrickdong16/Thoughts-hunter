async function request(method, path, data = null) {
    return new Promise((resolve) => {
        const options = {
            hostname: 'thoughts-radar-backend-production.up.railway.app',
            path: path,
            method: method,
            headers: { 'Content-Type': 'application/json' }
        };
        if (data) options.headers['Content-Length'] = Buffer.byteLength(JSON.stringify(data));

        const req = https.request(options, (res) => {
            let body = '';
            res.on('data', c => body += c);
            res.on('end', () => resolve({ ok: res.statusCode === 200 || res.statusCode === 201, status: res.statusCode, body }));
        });
        req.on('error', () => resolve({ ok: false }));
        if (data) req.write(JSON.stringify(data));
        req.end();
    });
}

